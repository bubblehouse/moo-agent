_SHEBANG_SAFE_ALIAS_RE = re.compile(r"^[a-z0-9][a-z0-9_?!-]*$")


def _is_shebang_safe_alias(name: str) -> bool:
    """
    True when ``name`` is safe to emit as a verb alias inside a shebang line.

    Verb shebangs are parsed by ``shlex.split``; aliases containing
    quotes, backslashes, or whitespace break shebang parsing.  HHG
    surfaces this via ``<SYNONYM WHAT WHATS WHAT'>`` which lowercases
    to the alias ``what'`` — the trailing apostrophe trips shlex's
    no-closing-quote check.

    :param name: A lowercased alias candidate.
    :returns: ``True`` if the alias is safe to emit; ``False`` to skip.
    """
    return bool(name) and bool(_SHEBANG_SAFE_ALIAS_RE.fullmatch(name))


def _py_str(s: str | None) -> str:
    """
    Render a string value as a Python string literal.

    :param s: A string, or ``None``.
    :returns: ``"None"`` for ``None``; ``repr(s)`` otherwise.
    """
    if s is None:
        return "None"
    return repr(s)


def _room_atom_to_var(atom: str) -> str:
    """
    Convert a ZIL room atom like ``WEST-OF-HOUSE`` to a Python variable name.

    :param atom: ZIL room atom (UPPER-KEBAB-CASE).
    :returns: A ``_r_<snake>`` Python identifier.
    """
    return "_r_" + atom.replace("-", "_").replace("?", "_Q").replace("!", "_B").lower()


def _obj_atom_to_var(atom: str) -> str:
    """
    Convert a ZIL object atom to a ``_o_<snake>`` Python variable name.

    :param atom: ZIL object atom.
    :returns: A ``_o_<snake>`` Python identifier.
    """
    return "_o_" + atom.replace("-", "_").replace("?", "_Q").replace("!", "_B").lower()


def _routine_to_filename(name: str) -> str:
    """
    Filename for a translated ZIL routine.

    Mirrors the verb-name transformations the translator applies in
    ``_shebang()`` so the on-disk filename matches the verb name
    registered in the database:

    - ``ACCESSIBLE?``  → ``is_accessible.py``  (predicate dot-syntax)
    - ``V-TAKE``       → ``take.py``           (substrate v-prefix dropped)
    - ``CRETIN-FCN``   → ``cretin_fcn.py``     (action handler, unchanged)

    :param name: The ZIL routine name.
    :returns: The Python filename to write.
    """
    upper = name.upper()
    if upper.endswith("?"):
        base = upper[:-1]
        if base and base[0].isalpha():
            return "is_" + base.lower().replace("-", "_") + ".py"
    if upper.startswith("V-"):
        return upper[2:].lower().replace("-", "_") + ".py"
    return name.replace("-", "_").replace("?", "_p").replace("!", "_b").lower() + ".py"


def _routine_to_verbname(name: str) -> str:
    """
    Convert a ZIL routine name to its kebab-case verb name.

    :param name: The ZIL routine name.
    :returns: The lower-kebab-case verb name.
    """
    return name.lower().replace("_", "-")


