    def _atom_dir(atom: str, is_room: bool) -> Path:
        """
        Directory for a per-room or per-object verb.

        :param atom: ZIL atom of the room or object.
        :param is_room: ``True`` when ``atom`` names a room.
        :returns: Target directory path.
        """
        slug = atom.lower().replace("-", "_")
