def _compute_display_names(rooms: dict[str, ZilRoom], objects: dict[str, ZilObject]) -> dict[str, str]:
    """
    Build a globally-unique atom → display-name map.

    Rooms keep their clean names; objects get ``(ATOM)`` appended on a
    DESC collision so ``--on "<display>"`` resolves to one Object.

    :param rooms: All ZIL rooms keyed by atom.
    :param objects: All ZIL objects keyed by atom.
    :returns: Atom → display-name map covering both rooms and objects.
    """
    from collections import Counter

    room_descs: set[str] = {r.desc.lower() for r in rooms.values() if r.desc}
    room_counts: Counter[str] = Counter(r.desc.lower() for r in rooms.values() if r.desc)
    obj_counts: Counter[str] = Counter(o.desc.lower() for o in objects.values() if o.desc)

    out: dict[str, str] = {}
    for atom, room in rooms.items():
        base = room.desc or atom.replace("-", " ").title()
        out[atom] = f"{base} ({atom})" if room.desc and room_counts[room.desc.lower()] > 1 else base
    for atom, obj in objects.items():
        # ZIL idiom: `(DESC "it")` means "no proper name, refer to me by
        # adjective + synonym" (HHG's SINK, BEDROOM-FURNISHINGS,
        # PROTAGONIST, …). Falling through to f"it (ATOM)" leaks the
        # placeholder into player-visible text ("There's nothing special
        # about the it.").  Prefer the first synonym instead — ZIL's
        # SYNONYM listing convention puts the canonical word first.
        # Falls back to the atom slug if no synonyms exist.
        desc = obj.desc
        if not desc or desc.lower() == "it":
            base = obj.synonyms[0].lower() if obj.synonyms else atom.replace("-", " ").lower()
            effective_desc = base
        else:
            base = desc
            effective_desc = desc.lower()
        collides = obj_counts[effective_desc] > 1 or effective_desc in room_descs
        out[atom] = f"{base} ({atom})" if collides else base
    return out


def _room_slug(atom: str) -> str:
    """
    Convert a ZIL atom to a lower-snake-case slug for directory paths.

    :param atom: ZIL atom.
    :returns: Lower-snake-case slug.
    """
    return atom.lower().replace("-", "_")


def _pre_name_for_v_routine(v_name: str) -> str:
    """
    Return the ``pre-<base>`` ZIL verb name for a V-routine.

    :param v_name: The V-routine name (e.g. ``"V-LAMP-ON"``).
    :returns: The ``pre-`` form (e.g. ``"pre-lamp-on"``).
    """
    return "pre-" + v_name.lower().removeprefix("v-")


def _snake_name_for_v_routine(v_name: str) -> str:
    """
    Snake-case a V-routine name for dot-syntax dispatch.

    :param v_name: The V-routine name (e.g. ``"V-LAMP-ON"``).
    :returns: The snake-case name (e.g. ``"lamp_on"``).
    """
    return v_name.lower().removeprefix("v-").replace("-", "_")


def _substrate_call_for_v_routine(v_name: str) -> str:
    """
    Emit the substrate dispatch expression for a V-routine.

    Falls back to ``invoke_verb("name")`` when the snake-name collides
    with a Python keyword (e.g. ``raise``, ``import``) so the generated
    dispatcher parses.

    :param v_name: The V-routine name (e.g. ``"V-RAISE"``).
    :returns: The method-call suffix attached to ``_.thing.``.
    """
    snake = _snake_name_for_v_routine(v_name)
    if verb_attr_safe(snake):
        return f"{snake}()"
    return f"invoke_verb({snake!r})"


def _substrate_dispatch_expr(v_name: str) -> str:
    """
    Emit the full dispatch expression for a V-routine, including receiver.

    Resolves the owning substrate class via :func:`substrate_receiver` so
    routines relocated off Thing (V-INVENTORY → Actor, V-SCORE → Actor,
    etc.) dispatch to the right receiver.  Without this, a dispatcher
    generated for `i` / `inv` / `score` would AttributeError because
    `_.thing.inventory` doesn't exist after the relocation.

    :param v_name: V-routine name (e.g. ``"V-INVENTORY"``).
    :returns: Full call expression (e.g. ``"context.player.inventory()"``).
    """
    snake = _snake_name_for_v_routine(v_name)
    receiver = substrate_receiver(snake)
    if verb_attr_safe(snake):
        return f"{receiver}.{snake}()"
    return f"{receiver}.invoke_verb({snake!r})"


def _resolve_object_room(atom: str, objects: dict, rooms: dict) -> str | None:
    """
    Return the outermost room atom containing ``atom``, or ``None``.

    Falls back to the first room listing ``atom`` in its
    ``(GLOBAL …)`` declaration when the container chain ends at a
    LOCAL-GLOBALS / GLOBAL-OBJECTS pseudo-container.

    :param atom: ZIL object atom to resolve.
    :param objects: All ZIL objects keyed by atom.
    :param rooms: All ZIL rooms keyed by atom.
    :returns: Containing room's atom, or ``None`` for orphans.
    """
    seen: set[str] = set()
    cur = atom
    while cur:
        if cur in seen:
            break
        seen.add(cur)
        if cur in rooms:
            return cur
        if cur in objects:
            cur = objects[cur].location
            continue
        break
    # Fall back: scan rooms for first GLOBAL declaration that mentions atom.
    for room_atom, room in rooms.items():
        if atom in (room.globals or []):
            return room_atom
    return None


