def _gen_rooms(rooms: dict[str, ZilRoom], display_names: dict[str, str], cfg: GameConfig) -> str:
    """
    Generate ``020_rooms.py`` — one stanza per ZilRoom.

    :param rooms: All ZIL rooms keyed by atom.
    :param display_names: Atom → display-name map.
    :param cfg: Per-game config (used for the trailing log banner).
    :returns: Complete ``020_rooms.py`` source.
    """
    lines = [
        _GENERATED_HEADER,
        '"""Zork room objects."""',
        "# pylint: disable=undefined-variable",
        "",
        "_rooms = {}  # keyed by ZIL atom — used by 040_exits.py",
        "",
        "def _ensure_parent(obj, parent):",
        "    # Defensive: re-attach parent if missing.  ``get_or_create_object``",
        "    # only sets parents on first create; this heals DBs created by an",
        "    # older bootstrap that had the object without correct parentage.",
        "    if parent.pk not in obj.parents.values_list('pk', flat=True):",
        "        obj.parents.add(parent)",
        "",
    ]

    for atom, room in rooms.items():
        var = _room_atom_to_var(atom)
        display_name = display_names[atom]
        lines.append(f"# {atom}")
        lines.append(f"{var}, _created = bootstrap.get_or_create_object(")
        lines.append(f"    {repr(display_name)}, unique_name=True,")
        lines.append(f"    parents=[_classes['room']],")
        lines.append(f")")
        lines.append(f"_ensure_parent({var}, _classes['room'])")

        if room.ldesc:
            lines.append(f"{var}.set_property('description', {_py_str(room.ldesc)})")
        elif room.fdesc:
            lines.append(f"{var}.set_property('description', {_py_str(room.fdesc)})")

        if room.fdesc and room.ldesc:
            lines.append(f"{var}.set_property('first_description', {_py_str(room.fdesc)})")

        # Room flags
        for flag in room.flags:
            if flag in ROOM_FLAG_PROPERTIES:
                prop, val = ROOM_FLAG_PROPERTIES[flag]
                lines.append(f"{var}.set_property({repr(prop)}, {repr(val)})")
        # Explicit dark/lit setter — even though the Room class
        # default is dark=True, a previous bootstrap run with an older
        # default (False) would leave existing rooms with the stale
        # inherited value, so always set it explicitly per-room.
        if "ONBIT" not in room.flags:
            lines.append(f"{var}.set_property('dark', True)")

        if room.value:
            lines.append(f"{var}.set_property('value', {room.value})")

        if room.globals:
            lines.append(f"{var}.set_property('global_scenery', {room.globals!r})")

        if room.pseudo:
            # ZIL ``(PSEUDO "WORD" ROUTINE ...)`` declares per-room scenery
            # words that dispatch to a substrate routine.  Store as a dict
            # ``{lowercase_word: snake_routine_name}`` so resolve_dobj_late
            # and do_command can match the player's typed word and invoke
            # the corresponding routine on Thing.
            pseudo_map = {word.lower(): rtn.lower().replace("-", "_") for word, rtn in room.pseudo}
            lines.append(f"{var}.set_property('pseudo', {pseudo_map!r})")

        if room.action:
            lines.append(f"# ACTION: {room.action} — see verbs/{_routine_to_filename(room.action)}")

        atom_alias = atom.lower().replace("-", "_")
        lines.append(f"{var}.add_alias({repr(atom_alias)})")
        # Also alias by ACTION-routine name when it differs from the atom.
        # ZIL code like ``<EQUAL? ,HERE ,LLD-ROOM>`` references the room
        # by its ROOM-FUNCTION name (LLD-ROOM is the action of
        # ENTRANCE-TO-HADES); without this alias the translator emits
        # ``_.thing.lld_room()`` (a routine call) which crashes
        # because LLD-ROOM is the room's lifecycle handler, not a
        # standalone helper.
        if room.action:
            action_alias = room.action.lower().replace("-", "_")
            if action_alias != atom_alias:
                lines.append(f"{var}.add_alias({repr(action_alias)})")
        lines.append(f"_rooms[{repr(atom)}] = {var}")
        lines.append("")

    lines.append(f"log.info({cfg.name!r} + ' rooms: %d', len(_rooms))")
    return "\n".join(lines) + "\n"


def _gen_objects(
    objects: dict[str, ZilObject],
    rooms: dict[str, ZilRoom],
    display_names: dict[str, str],
    cfg: GameConfig,
) -> str:
    """
    Generate ``030_objects.py`` — one stanza per ZilObject.

    :param objects: All ZIL objects keyed by atom.
    :param rooms: All ZIL rooms keyed by atom.
    :param display_names: Atom → display-name map.
    :param cfg: Per-game config; supplies ``player_avatar_atoms``.
    :returns: Complete ``030_objects.py`` source.
    """
    lines = [
        _GENERATED_HEADER,
        '"""Zork game objects."""',
        "# pylint: disable=undefined-variable",
        "",
        "_objects = {}  # keyed by ZIL atom",
        "",
        "def _ensure_parent(obj, parent):",
        "    # Defensive: re-attach parent if missing.  ``get_or_create_object``",
        "    # only sets parents on first create; this heals DBs created by an",
        "    # older bootstrap that had the object without correct parentage.",
        "    if parent.pk not in obj.parents.values_list('pk', flat=True):",
        "        obj.parents.add(parent)",
        "",
    ]
    # ZIL ``IN`` may reference an object declared later in dungeon.zil
    # (LEAFLET → MAILBOX is the canonical case).  Collect deferred location
    # fixups and apply them in a second pass after every object exists.
    deferred_locations: list[tuple[str, str]] = []  # (object_atom, location_atom)

    for atom, obj in objects.items():
        var = _obj_atom_to_var(atom)

        # Pick parent class
        if "ACTORBIT" in obj.flags:
            # Player-character placeholders (ME, ADVENTURER, PLAYER, WINNER)
            # stay on Actor so they don't get an anonymous Player row
            # via the NPC initialize hook.  Real NPCs (thief, cyclops,
            # troll, bat, ghosts) move to Actor NPC for the
            # personality ``act`` hook and player-record lifecycle.
            if atom in cfg.player_avatar_atoms:
                parent = "_classes['actor']"
            else:
                parent = "_classes['actor_npc']"
        elif "CONTBIT" in obj.flags:
            parent = "_classes['container']"
        else:
            parent = "_classes['thing']"

        # ``obvious`` is the parser's dobj-visibility flag.  Only INVISIBLE
        # gates parser visibility — NDESCBIT only suppresses auto-description
        # in room listings (PRINT-CONT skips describe but still recurses into
        # see-inside containers), so NDESCBIT objects must remain
        # parser-visible (obvious=True) to support ``examine table`` etc.
        obvious = "False" if "INVISIBLE" in obj.flags else "True"

        # Resolve location
        if obj.location and obj.location in rooms:
            loc_expr = f"_rooms[{repr(obj.location)}]"
        elif obj.location and obj.location in objects:
            # Forward reference to another object — start with None and
            # patch after every object has been created.
            loc_expr = "None"
            deferred_locations.append((atom, obj.location))
        elif obj.location:
            # Unknown atom (e.g. a global pseudo-container).  Best-effort
            # lookup through the running ``_objects`` dict.
            loc_expr = f"_objects.get({repr(obj.location)})  # {obj.location}"
        else:
            loc_expr = "None"

        display_name = display_names[atom]
        lines.append(f"# {atom}")
        lines.append(f"{var}, _created = bootstrap.get_or_create_object(")
        lines.append(f"    {repr(display_name)}, unique_name=False,")
        lines.append(f"    parents=[{parent}],")
        lines.append(f"    location={loc_expr},")
        lines.append(f")")
        lines.append(f"_ensure_parent({var}, {parent})")
        lines.append(f"{var}.obvious = {obvious}")
        lines.append(f"{var}.save()")

        # ZIL property semantics:
        #   DESC  → short label / name (printed by ``<PRINTD>`` / ``desc()``).
        #         Already on Object.name, so don't re-emit as a property.
        #   LDESC → long description shown when the player examines the
        #         object (V-EXAMINE).  Maps to ``description``.
        #   FDESC → "first-time-in-its-starting-location" description shown
        #         by the room-contents listing before the player has touched
        #         the object.  Maps to ``first_description``.
        # Don't fall back to obj.desc when LDESC is missing — describe_object
        # uses ``getp("description")`` to short-circuit the canonical "There is
        # a <X> here." template; falling back to desc makes every object's
        # getp("description") truthy and suppresses the canonical line.
        if obj.ldesc:
            lines.append(f"{var}.set_property('description', {_py_str(obj.ldesc)})")
        elif obj.fdesc:
            # Object has FDESC but no LDESC — older bootstraps copied FDESC
            # into ``description``, which suppresses the canonical
            # "There is a <X> here." template after touchbit.  Purge any
            # stale ``description`` row so describe_object falls through.
            lines.append(f"{var}.properties.filter(name='description').delete()")
        if obj.fdesc:
            lines.append(f"{var}.set_property('first_description', {_py_str(obj.fdesc)})")

        if obj.text:
            lines.append(f"{var}.set_property('text', {_py_str(obj.text)})")

        # Synonyms as aliases.  ME placeholder keeps its ``me``/``myself``
        # /``self`` aliases — verb code in 8+ files calls ``lookup("me")``
        # to compare against the player object, and removing the alias
        # would break those lookups.  ``find_object`` does only local
        # search; ``_o_me`` lives in limbo (location=None) so it isn't
        # findable that way regardless, and moo-core's pronoun fallback
        # at parse.py:536 still resolves ``me`` to caller.
        # Dedup against the atom-derived alias emitted later so an object
        # whose ZIL atom appears in its own synonyms list (e.g. INTNUM,
        # ALTAR, BONES) doesn't get the alias added twice.
        seen_aliases: set[str] = set()
        for syn in obj.synonyms:
            if syn in seen_aliases:
                continue
            seen_aliases.add(syn)
            lines.append(f"{var}.add_alias({repr(syn)})")
            # ZIL truncates dictionary entries to 6 chars; the
            # per-game synonym_expansions map adds the full word as a
            # second alias when a truncation matches a known stem
            # (HHG's ASPIRI → aspirin, ANALGE → analgesic, …).
            full = cfg.synonym_expansions.get(syn.upper())
            if full and full.lower() not in seen_aliases:
                seen_aliases.add(full.lower())
                lines.append(f"{var}.add_alias({repr(full.lower())})")

        # Adjectives stored as property
        if obj.adjectives:
            lines.append(f"{var}.set_property('adjectives', {obj.adjectives!r})")

        # Flag properties.  ``obvious`` is intrinsic and was set above via
        # the model field, so we skip both ``obvious`` and ``invisible``
        # (the runtime SDK routes ``flag('invisible')`` through ``obvious``
        # — keeping the property would let stale state mask the field).
        for flag in obj.flags:
            if flag in FLAG_PROPERTIES:
                prop, val = FLAG_PROPERTIES[flag]
                if prop in ("obvious", "invisible"):
                    continue
                lines.append(f"{var}.set_property({repr(prop)}, {repr(val)})")

        if obj.capacity:
            lines.append(f"{var}.set_property('capacity', {obj.capacity})")
        if obj.size != 5:
            lines.append(f"{var}.set_property('size', {obj.size})")
        if obj.value:
            lines.append(f"{var}.set_property('value', {obj.value})")
        if obj.tvalue:
            lines.append(f"{var}.set_property('tvalue', {obj.tvalue})")
        if obj.vtype:
            lines.append(f"{var}.set_property('vtype', {obj.vtype!r})")

        if obj.action:
            lines.append(f"# ACTION: {obj.action} — see verbs/{_routine_to_filename(obj.action)}")

        atom_alias = atom.lower().replace("-", "_")
        if atom_alias not in seen_aliases:
            lines.append(f"{var}.add_alias({repr(atom_alias)})")
        lines.append(f"_objects[{repr(atom)}] = {var}")
        lines.append("")

    if deferred_locations:
        lines.append("# Resolve forward IN references that pointed at later-declared objects.")
        for obj_atom, loc_atom in deferred_locations:
            lines.append(f"_objects[{obj_atom!r}].location = _objects[{loc_atom!r}]; _objects[{obj_atom!r}].save()")
        lines.append("")
    # Atom → PK map on System Object so scenery lookups can disambiguate
    # objects whose ZIL atom is shadowed by another object's SYNONYM
    # alias (HHG: CANOPY.SYNONYM includes WINDOW, but `WINDOW` the atom
    # is a distinct LOCAL-GLOBALS object).  Used by
    # ``resolve_dobj_late.py`` to pick the right atom-specific object.
    lines.append("_.set_property('zatom_pk_map', {atom: o.pk for atom, o in _objects.items()})")
    lines.append(f"log.info({cfg.name!r} + ' objects: %d', len(_objects))")
    return "\n".join(lines) + "\n"


def _gen_exits(rooms: dict[str, ZilRoom], cfg: GameConfig | None = None) -> str:
    """
    Emit ``040_exits.py``.

    Each room's ``exits`` list and each destination's ``entrances``
    list are accumulated in Python locals first, then written once at
    the end of the room block — avoiding the get/append/set-per-exit
    pattern that grew the lists 8× on every sync.

    :param rooms: All ZIL rooms keyed by atom.
    :returns: Complete ``040_exits.py`` source.
    """
    lines = [
        _GENERATED_HEADER,
        '"""Zork exit objects connecting rooms."""',
        "# pylint: disable=undefined-variable",
        "",
        "_entrances: dict[int, list] = {}  # room pk → entry-Object list (built up below)",
        "",
    ]

    for atom, room in rooms.items():
        if not room.exits:
            continue
        room_var = _room_atom_to_var(atom)
        lines.append(f"# Exits from {atom}")
        lines.append(f"_room_exits = []")

        for ex in room.exits:
            direction_lower = ex.direction.lower()
            aliases = DIRECTION_ALIASES.get(ex.direction, [direction_lower])
            # Use atom (always unique) rather than DESC — when ZIL games
            # share a DESC across rooms (e.g. FOREST-1/2/3 are all "Forest"),
            # the exit names would collide under unique_name=True and the
            # exits would all attach to whichever Object was created first.
            exit_name = f"{direction_lower} from {atom}"

            lines.append(f"_e, _created = bootstrap.get_or_create_object(")
            lines.append(f"    {repr(exit_name)}, unique_name=True,")
            lines.append(f"    parents=[_classes['exit']],")
            lines.append(f")")
            lines.append(f"_e.set_property('source', {room_var})")

            if ex.per_routine:
                lines.append(f"_e.set_property('dest', None)")
                lines.append(f"_e.set_property('exit_routine', {repr(ex.per_routine)})")
                lines.append(f"# PER: {ex.per_routine} — needs custom verb in verbs/")
            elif ex.dest and ex.dest in rooms:
                dest_var = _room_atom_to_var(ex.dest)
                lines.append(f"_e.set_property('dest', {dest_var})")
                # Per-game ACTION-routine guards that the auto-translator
                # can't see (TROLL-MELEE blocking all 4 exits while
                # TROLL-FLAG is set, etc.).  Game config overrides win
                # over the ZIL-side flag if both are present.
                override = None
                if cfg is not None:
                    override = cfg.exit_condition_overrides.get((atom, ex.direction.upper()))
                if override is not None:
                    flag, nogo = override
                    lines.append(f"_e.set_property('condition_flag', {repr(flag)})")
                    lines.append(f"_e.set_property('nogo_msg', {_py_str(nogo)})")
                elif ex.condition:
                    lines.append(f"_e.set_property('condition_flag', {repr(ex.condition)})")
                if ex.else_message and override is None:
                    lines.append(f"_e.set_property('nogo_msg', {_py_str(ex.else_message)})")
            elif ex.message:
                lines.append(f"_e.set_property('dest', None)")
                lines.append(f"_e.set_property('nogo_msg', {_py_str(ex.message)})")
            elif ex.dest:
                # dest atom not found in rooms dict (e.g. a non-room destination)
                lines.append(f"_e.set_property('dest', None)  # unknown dest: {ex.dest}")
                lines.append(f"_e.set_property('nogo_msg', 'You cannot go that way.')")
            else:
                lines.append(f"_e.set_property('dest', None)")
                lines.append(f"_e.set_property('nogo_msg', 'You cannot go that way.')")

            for alias in aliases:
                lines.append(f"_e.add_alias({repr(alias)})")

            lines.append(f"_e.save()")
            lines.append(f"_room_exits.append(_e)")

            if ex.dest and ex.dest in rooms:
                dest_var = _room_atom_to_var(ex.dest)
                lines.append(f"_entrances.setdefault({dest_var}.pk, []).append(_e)")

            lines.append("")

        lines.append(f"{room_var}.set_property('exits', _room_exits)")
        lines.append("")

    # Flush all accumulated entrances at end so a single dest collects every
    # incoming exit before the property is written.
    lines.append("for _room_pk, _ents in _entrances.items():")
    lines.append("    _r = _rooms_by_pk.get(_room_pk) if '_rooms_by_pk' in dir() else None")
    lines.append("    if _r is None:")
    lines.append("        from moo.core.models.object import Object as _Object")
    lines.append("        _r = _Object.global_objects.get(pk=_room_pk)")
    lines.append("    _r.set_property('entrances', _ents)")
    lines.append("")

    return "\n".join(lines) + "\n"


